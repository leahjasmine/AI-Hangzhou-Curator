let ipGuideIndex = 0;

const langToggle = document.querySelector('#langToggle');
const toolTabs = document.querySelectorAll('.tool-tab');
const promptInput = document.querySelector('#promptInput');
const generateBtn = document.querySelector('#generateBtn');
const answerCard = document.querySelector('#answerCard');
const answerText = document.querySelector('#answerText');
const interactiveCards = document.querySelectorAll('.interactive-card[data-demo-type]');
const ipGuide = document.querySelector('[data-ip-guide]');

function bounceElement(element) {
  if (!element) return;
  element.classList.remove('is-bouncing');
  void element.offsetWidth;
  element.classList.add('is-bouncing');
  window.setTimeout(() => element.classList.remove('is-bouncing'), 620);
}

function revealAnswer() {
  answerCard.classList.remove('revealed');
  void answerCard.offsetWidth;
  answerCard.classList.add('revealed');
}

function translatePage(language) {
  currentLanguage = language;
  document.documentElement.lang = language === 'zh' ? 'zh-CN' : 'en';
  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = copy[language][key] || element.textContent;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    element.placeholder = copy[language][key] || element.placeholder;
  });
  langToggle.textContent = language === 'zh' ? 'EN' : '中';
}

function setActiveTool(type) {
  currentTool = type;
  toolTabs.forEach((tab) => {
    const isActive = tab.dataset.type === type;
    tab.classList.toggle('active', isActive);
    if (isActive) bounceElement(tab);
  });
}

function activateDemoCard(card) {
  const type = card.dataset.demoType || 'qa';
  const prompt = currentLanguage === 'en'
    ? englishDemoPrompts[type] || englishDemoPrompts.qa
    : card.dataset.demoPrompt || '';
  setActiveTool(type);
  promptInput.value = prompt;
  document.querySelector('#curator')?.scrollIntoView({ behavior: 'smooth', block: 'start' });
  window.setTimeout(() => {
    promptInput.focus();
    bounceElement(promptInput);
  }, 420);
}

function activateIpGuide() {
  const prompts = ipGuidePrompts[currentLanguage] || ipGuidePrompts.zh;
  const guide = prompts[ipGuideIndex % prompts.length];
  ipGuideIndex += 1;
  setActiveTool(guide.type);
  promptInput.value = guide.prompt;
  ipGuide?.classList.add('is-guiding');
  bounceElement(ipGuide);
  document.querySelector('#curator')?.scrollIntoView({ behavior: 'smooth', block: 'start' });
  window.setTimeout(() => {
    promptInput.focus();
    bounceElement(promptInput);
    ipGuide?.classList.remove('is-guiding');
  }, 520);
}

async function generateAnswer() {
  bounceElement(generateBtn);
  const prompt = promptInput.value.trim();
  if (!prompt) {
    answerText.textContent = copy[currentLanguage].emptyPrompt;
    revealAnswer();
    return;
  }

  answerCard.classList.add('loading');
  answerText.textContent = copy[currentLanguage].loading;

  try {
    const response = await fetch('/api/ai', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        type: currentTool,
        prompt,
        language: currentLanguage,
      }),
    });
    const data = await response.json();
    const debugText = data.ok === false && data.error ? `\n\n接口提示：${data.error}` : '';
    answerText.textContent = `${data.answer || copy[currentLanguage].error}${debugText}`;
    revealAnswer();
  } catch (error) {
    answerText.textContent = `${copy[currentLanguage].error}\n\n接口提示：${error.message}`;
    revealAnswer();
  } finally {
    answerCard.classList.remove('loading');
  }
}

langToggle.addEventListener('click', () => {
  bounceElement(langToggle);
  translatePage(currentLanguage === 'zh' ? 'en' : 'zh');
});

toolTabs.forEach((tab) => {
  tab.addEventListener('click', () => setActiveTool(tab.dataset.type));
});

interactiveCards.forEach((card) => {
  card.addEventListener('click', (event) => {
    event.preventDefault();
    activateDemoCard(card);
  });
  card.addEventListener('keydown', (event) => {
    if (event.key === 'Enter' || event.key === ' ') {
      event.preventDefault();
      activateDemoCard(card);
    }
  });
});

if (ipGuide) {
  ipGuide.addEventListener('click', activateIpGuide);
  ipGuide.addEventListener('keydown', (event) => {
    if (event.key === 'Enter' || event.key === ' ') {
      event.preventDefault();
      activateIpGuide();
    }
  });
}

generateBtn.addEventListener('click', generateAnswer);

promptInput.addEventListener('keydown', (event) => {
  if ((event.ctrlKey || event.metaKey) && event.key === 'Enter') {
    generateAnswer();
  }
});

translatePage(currentLanguage);
